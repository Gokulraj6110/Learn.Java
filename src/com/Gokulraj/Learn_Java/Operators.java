package com.Gokulraj.Learn_Java;

public class Operators {
    public static void main(String[] args){
        byte a = 10;
        byte b = 20;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a > b);
    }
}
